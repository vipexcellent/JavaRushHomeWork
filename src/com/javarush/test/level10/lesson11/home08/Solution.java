package com.javarush.test.level10.lesson11.home08;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.util.ArrayList;

/* Массив списков строк
Создать массив, элементами которого будут списки строк. Заполнить массив любыми данными и вывести их на экран.
*/

public class Solution
{
    public static void main(String[] args)
    {
        ArrayList<String>[] arrayOfStringList =  createList();
        printList(arrayOfStringList);
    }

    public static ArrayList<String>[] createList()
    {
        ArrayList<String>[] arrayOfStringList = new ArrayList[2];
        arrayOfStringList[0] = new ArrayList<String>();

        for (int i = 0; i < 5; i++)
        {
            arrayOfStringList[0].add(Integer.toString(i));
        }

        arrayOfStringList[1] = new ArrayList<String>();

        for (int i = 0; i < 6; i++)
        {
            arrayOfStringList[1].add(Integer.toString(i));
        }

        return arrayOfStringList;
    }

    public static void printList(ArrayList<String>[] arrayOfStringList)
    {
        for (ArrayList<String> list: arrayOfStringList)
        {
            for (String s : list)
            {
                System.out.println(s);
            }
        }
    }
}